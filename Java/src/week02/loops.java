public class loops {

	public static void main(String[] args) {
		//for loop setup
		/*
		 * for (int i = 0; i < value; i++) {
		 * 	do something;
		 * }
		 */
		
		// while setup
		/*
		 * int x = 0;	// assignment must happen prior to while loop
		 * while (x < value) {
		 * 
		 * 	do something;
		 * 	
		 * 	x++;		// x must be increased or while loop will be indefinite
		 * }
		 */
		
		// do-while setup
		/*
		 * int x = 0;
		 * do {
		 * 	do something;
		 * 	x++;
		 * } while (x < value); // semicolon added after the while call
		 */
	}

}
