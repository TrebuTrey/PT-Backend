package week03;

public class arrays {

	public static void main(String[] args) {
		//info on arrays
		/*
		 * arrays must consist of only one Type of data
		 * the length of the array must be called at time of instantiation
		 * 
		 * example
		 * 
		 * int length = 5;
		 * String[] students = new String[length];
		 * 
		 * students[0] = "Some Name";
		 * students[1] = "Another Name";
		 */
		// Arrays are objects with their own parameters that can be called. Common option seen
		// will be array.length

		
		// Enhanced for-loops
		/*
		 * These work as forEach loops on objects such as arrays
		 * 
		 * for (String student : students) {
		 * 	println(student);
		 * }
		 */
	}

}
