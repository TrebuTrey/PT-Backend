package week05;

public class InheritanceAbstractionPolymorphism {
	public static void main(String[] args) {
		/*
		 * Inheritance
		 * 
		 * The usage of a blueprint class to pass variables and methods
		 * of narrower classes that are likely to use
		 * 
		 * example:
		 * 
		 * public class ChildClass extends ParentClass {
		 * 	//TODO
		 * }
		 * 
		 * Abstraction
		 * 
		 * Given to classes but there is no implementation of said class.
		 * Very useful for child inheritance
		 * 
		 * example:
		 * 
		 * public abstract MethodName();
		 * 
		 * 
		 * Polymorphism
		 * 
		 * The ability to utilize the same name but take on different
		 * forms. When a method or variable is denoted as abstract,
		 * the child class can vary in implementation of the 
		 * expected method.
		 * 
		 */
	}
}
